def get_bat_filename(site):
    import re
    def safe(s):
        s = re.sub(r'[<>:"/\\|?*&]', '', s)
        s = re.sub(r'\s+', '_', s.strip())
        return s
    return f"{safe(site['county'])}_{safe(site['name'])}_RFSS{site['rfss']}_Site{site['site_dec']}.bat"

def launch_bat(site):
    bat_dir = os.path.dirname(os.path.abspath(__file__))
    bat_name = get_bat_filename(site)
    bat_path = os.path.join(bat_dir, bat_name)
    if not os.path.exists(bat_path):
        messagebox.showerror("File Not Found",
            f"Could not find:\n{bat_path}\n\nMake sure the .bat files are in the same folder as this script.")
        return
    try:
        subprocess.Popen(["cmd.exe", "/c", bat_path], cwd=bat_dir,
                         creationflags=subprocess.CREATE_NEW_CONSOLE)
    except Exception as e:
        messagebox.showerror("Launch Error", str(e))


